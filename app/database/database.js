      if (!name.startsWith('EndOfMatchAward')) {
        for (var j = 0; j < valArray.length; j++) {
          if (valArray[j].length > 0) {
            var playerID = j + 1;
            players[playerID].gameStats[name] = valArray[j][0].m_value;
          }
        }
      }
